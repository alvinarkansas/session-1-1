const umur = 13

// apabila umur 17 taun ke atas maka print 'sudah bisa bikin KTP'
// apabila umur 10 - 17 taun maka print 'hampir bisa bikin KTP'
// apabila umur kurang dari 10 taun maka print 'belum cukup umur'

if (umur >= 17 ) {
  console.log("sudah bisa bikin KTP");
} else if (umur >= 10) {
  console.log("hampir bisa bikin KTP");
} else   {
  console.log("belum cukup umur");
}
